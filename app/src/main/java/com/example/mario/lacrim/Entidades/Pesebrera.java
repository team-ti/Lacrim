package com.example.mario.lacrim.Entidades;

public class Pesebrera {
    
    private String id_pes;
    private String nombre_pes;
    private String encargado_pes;
    private String ciudad_pes;
    private String telefono_pes;

    public Pesebrera() {
        this.id_pes = id_pes;
        this.nombre_pes = nombre_pes;
        this.encargado_pes = encargado_pes;
        this.ciudad_pes = ciudad_pes;
        this.telefono_pes = telefono_pes;
    }

    public String getId_pes() {
        return id_pes;
    }

    public void setId_pes(String id_pes) {
        this.id_pes = id_pes;
    }

    public String getNombre_pes() {
        return nombre_pes;
    }

    public void setNombre_pes(String nombre_pes) {
        this.nombre_pes = nombre_pes;
    }

    public String getEncargado_pes() {
        return encargado_pes;
    }

    public void setEncargado_pes(String encargado_pes) {
        this.encargado_pes = encargado_pes;
    }

    public String getCiudad_pes() {
        return ciudad_pes;
    }

    public void setCiudad_pes(String ciudad_pes) {
        this.ciudad_pes = ciudad_pes;
    }

    public String getTelefono_pes() {
        return telefono_pes;
    }

    public void setTelefono_pes(String telefono_pes) {
        this.telefono_pes = telefono_pes;
    }
}
